fun getDeeplApiKey(): String {
    val props = Properties()
    val file = rootProject.file("local.properties")
    if (file.exists()) {
        props.load(file.inputStream())
        return props.getProperty("DEEPL_API_KEY") ?: ""
    }
    return ""
}